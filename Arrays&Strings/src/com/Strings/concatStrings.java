package com.Strings;

public class concatStrings {
    public static void main(String[] args) {
        String s1 = "Sid";
        String s2 = "ddharth";
        String s3 = "parmar";


        String s4 = s1.concat(s2);
        System.out.println(s4); //Siddharth

        String s5 = "Siddharth";


    }
}
